public class VariableUseExample {

    public static void main(String[] args) {
        int hour = 3;
        int minute = 5;
        System.out.println(hour + "시간 " + minute + "분");

        int totalMinute = 3 * 60 + 5;
        System.out.println("총 " + totalMinute + "분");
    }
}
